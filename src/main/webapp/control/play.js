$(function() {
	$.getUrlParam = function(name) {
		var reg = new RegExp("(^|&)" + name + "=([^&]*)(&|$)");
		var r = window.location.search.substr(1).match(reg);
		if (r != null)
			return unescape(r[2]);
		return null;
	}
	var filepath = $.getUrlParam('url');
	$("#videoSource").attr("src", "api/play/" + filepath);
});